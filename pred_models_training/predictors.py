_VADER = SentimentIntensityAnalyzer()

def nrc_doc_score_from_text(text: str) -> Dict[str, float]:
    emo = NRCLex(text or "")
    scores = dict(emo.raw_emotion_scores or {})
    # Normalize by word count to reduce length effects
    wc = max(_word_count(text), 1)
    return {k: v / wc for k, v in scores.items()}

def get_sentiment_class(compound: float) -> str:
    # common VADER thresholds
    if compound >= 0.05:
        return "positive"
    if compound <= -0.05:
        return "negative"
    return "neutral"

def extract_sentiment_features_from_statement(statement: str) -> Dict[str, float]:
    vader = _VADER.polarity_scores(statement or "")
    nrc = nrc_doc_score_from_text(statement or "")
    feats = {
        "vader_neg": vader["neg"],
        "vader_neu": vader["neu"],
        "vader_pos": vader["pos"],
        "vader_compound": vader["compound"],
        "word_count": float(_word_count(statement)),
        "exclamation_count": float((statement or "").count("!")),
        "question_count": float((statement or "").count("?")),
        "uppercase_ratio": float(sum(1 for c in (statement or "") if c.isupper()) / max(len(statement or ""), 1)),
    }
    # Add NRC emotion scores (sparse)
    for k, v in nrc.items():
        feats[f"nrc_{k}"] = float(v)
    return feats

def create_sentiment_feature_dataframe(statements: List[str]) -> pd.DataFrame:
    rows = [extract_sentiment_features_from_statement(s) for s in statements]
    return pd.DataFrame(rows).fillna(0.0)

def _features_for_inference(statements: List[str], scaler: Optional[StandardScaler]=None) -> Tuple[np.ndarray, List[str]]:
    df = create_sentiment_feature_dataframe(statements)
    cols = sorted(df.columns)
    X = df[cols].to_numpy(dtype=np.float32)
    if scaler is not None:
        X = scaler.transform(X)
    return X, cols

