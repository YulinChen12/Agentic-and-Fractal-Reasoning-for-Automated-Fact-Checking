def _hf_predict_sentence_labels(
    sentences: List[str],
    tokenizer,
    model,
) -> Tuple[List[int], List[float]]:
    # returns labels + confidences per sentence
    labels = []
    confs = []
    with torch.no_grad():
        for s in sentences:
            enc = tokenizer(
                s,
                truncation=True,
                padding=True,
                max_length=256,
                return_tensors="pt",
            )
            out = model(**enc)
            logits = out.logits
            probs = torch.softmax(logits, dim=-1).cpu().numpy()[0]
            lab = int(np.argmax(probs))
            labels.append(lab)
            confs.append(float(np.max(probs)))
    return labels, confs

def predict_article_reputation(article_text: Optional[str] = None, sentences: Optional[List[str]] = None) -> Dict:
    """
    Sentence-level majority vote. Returns:
    {
      "label": "low|medium|high" (depends on your fine-tuned label order),
      "confidence": 0-1,
      "sentence_votes": Counter,
      "sentences": [{"text":..., "label_id":..., "confidence":...}, ...]
    }
    """
    _ensure_reputation()
    if sentences is None:
        if article_text is None:
            raise ValueError("Provide article_text or sentences.")
        sentences = sent_tokenize(article_text)
    label_ids, confs = _hf_predict_sentence_labels(sentences, _REP_TOK, _REP_MODEL)
    votes = Counter(label_ids)
    top_id, top_ct = votes.most_common(1)[0] if votes else (0, 0)
    avg_conf = float(np.mean([c for i, c in zip(label_ids, confs) if i == top_id])) if votes else None

    # NOTE: update mapping to match your fine-tuned model config id2label if needed
    id2label = getattr(_REP_MODEL.config, "id2label", None) or {0: "low", 1: "medium", 2: "high"}
    label = id2label.get(top_id, str(top_id))

    return {
        "label": label,
        "confidence": avg_conf,
        "sentence_votes": dict(votes),
        "sentences": [{"text": s, "label_id": i, "confidence": c} for s, i, c in zip(sentences, label_ids, confs)],
        "meta": {"model": "DistilBERT sentence vote"},
    }

