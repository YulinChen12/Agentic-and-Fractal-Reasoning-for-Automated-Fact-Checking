def _word_count(s: str) -> int:
    return len(re.findall(r"\b\w+\b", s or ""))

def text_of(r: pd.Series) -> str:
    # combine text fields you want the model to learn from
    return " ".join([
        str(r.get("statement", "")),
        str(r.get("context", "")),
        str(r.get("justification", "")),
    ]).strip()

def first_subject(s) -> str:
    # LIAR topic field may contain semicolon/comma separated subjects
    if not isinstance(s, str):
        return "unknown"
    parts = re.split(r"[;,]", s)
    x = parts[0].strip().lower() if parts else ""
    return x if x else "unknown"

def evidence_anchors(text: str) -> Dict[str, int]:
    # simple pattern counts (same spirit as notebook)
    patterns = {
        "quotes": len(re.findall(r"\"", text)),
        "numbers": len(re.findall(r"\b\d+(\.\d+)?\b", text)),
        "links": len(re.findall(r"https?://", text)),
        "proper_nouns": 0,
    }
    try:
        tokens = nltk.word_tokenize(text)
        tags = nltk.pos_tag(tokens)
        patterns["proper_nouns"] = sum(1 for _, t in tags if t == "NNP")
    except Exception:
        patterns["proper_nouns"] = 0
    return patterns

