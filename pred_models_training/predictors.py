def predict_news_coverage(article_text: str) -> Dict:
    """
    Returns {"label": <topic>, "confidence": float, "meta": {...}}
    """
    _ensure_topic()
    proba = None
    label = _TOPIC_PIPELINE.predict([article_text])[0]
    # LinearSVC doesn't expose predict_proba; provide a heuristic confidence
    try:
        scores = _TOPIC_PIPELINE.decision_function([article_text])[0]
        # convert margin to 0-1-ish via softmax
        ex = np.exp(scores - np.max(scores))
        proba = float(np.max(ex / np.sum(ex)))
    except Exception:
        proba = None
    return {"label": label, "confidence": proba, "meta": {"model": "LinearSVC+TFIDF"}}

def predict_intent(title: str = "", body: str = "") -> Dict:
    _ensure_intent()
    
    # _INTENT_PIPELINE is actually a dictionary from train_all.py
    vectorizer = _INTENT_PIPELINE["vectorizer"]
    proto_mat = _INTENT_PIPELINE["proto_mat"]
    class_names = _INTENT_PIPELINE["class_names"]

    text = (title or "").strip() + " " + (body or "").strip()
    
    # Transform text and normalize
    z = vectorizer.transform([text])
    from sklearn.preprocessing import normalize
    zn = normalize(z, norm="l2", axis=1)
    
    # Calculate cosine similarity against the prototypes
    scores = (zn @ proto_mat.T).ravel()
    
    # Find the highest scoring intent
    best_idx = int(np.argmax(scores))
    label = class_names[best_idx]
    conf = float(scores[best_idx])
    
    return {"label": label, "confidence": conf, "meta": {"model": "TFIDF+Prototypes"}}

def predict_sensationalism(statement: str, justification: str = "") -> Dict:
    """
    Predicts sensationalism using the trained SVC pipeline.
    Artifact dict expects: {"tfidf": ..., "model": ..., "evidence_patterns": ...}
    """
    _ensure_sens()
    
    # Load the specific artifacts saved by train_all.py
    tfidf = _SENS_MLP["tfidf"]
    model = _SENS_MLP["model"]
    ev_patterns = _SENS_MLP.get("evidence_patterns", [])

    # Combine text (matches train_all.py: statement + " " + context)
    combined_text = str(statement or "") + " " + str(justification or "")

    # Extract TF-IDF features
    X_tfidf = tfidf.transform([combined_text])

    # Calculate evidence anchor density exactly as train_all.py does
    s = str(statement or "")
    token_re = re.compile(r"[A-Za-z]+")
    ev_re = [re.compile(p, flags=re.I) for p in ev_patterns]

    hits = sum(len(r.findall(s)) for r in ev_re)
    toks = max(1, len(token_re.findall(s)))
    dens = np.log1p(hits) / max(10, toks)
    ev_score = float(np.clip(dens, 0.0, 0.2))

    X_ev = np.array([[ev_score]])

    # Combine features using hstack (Scipy Sparse Matrix)
    X = hstack([X_tfidf, X_ev])

    # Predict (model outputs 1 for sensational, 0 for neutral based on your THRESHOLD)
    pred_val = model.predict(X)[0]
    label = "sensational" if pred_val == 1 else "neutral"

    # Get Confidence Score
    conf = None
    if hasattr(model, "predict_proba"):
        conf = float(np.max(model.predict_proba(X)[0]))

    return {
        "label": label, 
        "confidence": conf, 
        "meta": {"model": "SVC+TFIDF+Anchors"}
    }

def predict_sentiment(statement: str) -> Dict:
    """
    Artifact is dict: {"scaler": ..., "clf": ..., "cols": [...]}
    """
    _ensure_sentiment()
    scaler = _SENTIMENT_MLP["scaler"]
    clf = _SENTIMENT_MLP["clf"]
    cols = _SENTIMENT_MLP["cols"]
    feats = extract_sentiment_features_from_statement(statement or "")
    df = pd.DataFrame([feats]).fillna(0.0)
    for c in cols:
        if c not in df.columns:
            df[c] = 0.0
    X = df[cols].to_numpy(dtype=np.float32)
    X = scaler.transform(X)
    label = clf.predict(X)[0]
    conf = None
    if hasattr(clf, "predict_proba"):
        conf = float(np.max(clf.predict_proba(X)[0]))
    return {"label": label, "confidence": conf, "meta": {"model": "MLP+VADER+NRC"}}

def _hf_predict_sentence_labels(
    sentences: List[str],
    tokenizer,
    model,
) -> Tuple[List[int], List[float]]:
    # returns labels + confidences per sentence
    labels = []
    confs = []
    with torch.no_grad():
        for s in sentences:
            enc = tokenizer(
                s,
                truncation=True,
                padding=True,
                max_length=256,
                return_tensors="pt",
            )
            out = model(**enc)
            logits = out.logits
            probs = torch.softmax(logits, dim=-1).cpu().numpy()[0]
            lab = int(np.argmax(probs))
            labels.append(lab)
            confs.append(float(np.max(probs)))
    return labels, confs

def predict_article_reputation(article_text: Optional[str] = None, sentences: Optional[List[str]] = None) -> Dict:
    """
    Sentence-level majority vote. Returns:
    {
      "label": "low|medium|high" (depends on your fine-tuned label order),
      "confidence": 0-1,
      "sentence_votes": Counter,
      "sentences": [{"text":..., "label_id":..., "confidence":...}, ...]
    }
    """
    _ensure_reputation()
    if sentences is None:
        if article_text is None:
            raise ValueError("Provide article_text or sentences.")
        sentences = sent_tokenize(article_text)
    label_ids, confs = _hf_predict_sentence_labels(sentences, _REP_TOK, _REP_MODEL)
    votes = Counter(label_ids)
    top_id, top_ct = votes.most_common(1)[0] if votes else (0, 0)
    avg_conf = float(np.mean([c for i, c in zip(label_ids, confs) if i == top_id])) if votes else None

    # NOTE: update mapping to match your fine-tuned model config id2label if needed
    id2label = getattr(_REP_MODEL.config, "id2label", None) or {0: "low", 1: "medium", 2: "high"}
    label = id2label.get(top_id, str(top_id))

    return {
        "label": label,
        "confidence": avg_conf,
        "sentence_votes": dict(votes),
        "sentences": [{"text": s, "label_id": i, "confidence": c} for s, i, c in zip(sentences, label_ids, confs)],
        "meta": {"model": "DistilBERT sentence vote"},
    }

