def analyze_complete_article(article_text: str) -> Dict:
    """
    One call for all 6 factors.
    """
    # crude split: if user provides "Title:" header, parse it
    title = ""
    body = article_text
    m = re.search(r"^\s*Title:\s*(.+)$", article_text, flags=re.M)
    if m:
        title = m.group(1).strip()
        # remove the Title line from body
        body = re.sub(r"^\s*Title:.*$", "", article_text, flags=re.M).strip()

    out = {
        "predictions": {
            "news_coverage": predict_news_coverage(article_text),
            "intent": predict_intent(title=title, body=body),
            "sensationalism": predict_sensationalism(statement=title or article_text, justification=body),
            "sentiment": predict_sentiment(statement=title or article_text),
            "reputation": predict_article_reputation(article_text=article_text),
            "stance": predict_article_stance(article_text=article_text),
        }
    }
    return out

def analyze_whole_article(article_text: str) -> Dict:
    """
    Kept for compatibility with your notebook naming.
    Currently aliases analyze_complete_article.
    """
    return analyze_complete_article(article_text)
