def text_of(row: pd.Series) -> str:
    # Notebook uses "title" + "body" style sometimes; keep flexible:
    parts = []
    for k in ["title", "Title", "headline"]:
        if k in row and isinstance(row[k], str) and row[k].strip():
            parts.append(row[k].strip())
            break
    for k in ["body", "Body", "content", "text"]:
        if k in row and isinstance(row[k], str) and row[k].strip():
            parts.append(row[k].strip())
            break
    if not parts:
        # fallback: join all string columns
        for v in row.values:
            if isinstance(v, str) and v.strip():
                parts.append(v.strip())
    return "\n\n".join(parts)

def _word_count(s: str) -> int:
    return len(re.findall(r"\b\w+\b", s or ""))

def first_subject(sentence: str) -> str:
    # lightweight subject heuristic: first noun/pronoun after tag
    try:
        tokens = nltk.word_tokenize(sentence)
        tags = nltk.pos_tag(tokens)
        for w, t in tags:
            if t in {"NN", "NNS", "NNP", "NNPS", "PRP"}:
                return w
    except Exception:
        pass
    return ""

def evidence_anchors(text: str) -> Dict[str, int]:
    # simple pattern counts (same spirit as notebook)
    patterns = {
        "quotes": len(re.findall(r"\"", text)),
        "numbers": len(re.findall(r"\b\d+(\.\d+)?\b", text)),
        "links": len(re.findall(r"https?://", text)),
        "proper_nouns": 0,
    }
    try:
        tokens = nltk.word_tokenize(text)
        tags = nltk.pos_tag(tokens)
        patterns["proper_nouns"] = sum(1 for _, t in tags if t == "NNP")
    except Exception:
        patterns["proper_nouns"] = 0
    return patterns

